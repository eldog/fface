#!/bin/bash

################################################################################
#
#   setup.sh
#
#   Installs all the required libraries.
#
################################################################################

set -o errexit
set -o nounset
set -o verbose

################################################################################
#
# Find the root of our repo
#
################################################################################

readonly REPO_ROOT_DIR="$(readlink -f -- "$(dirname -- "${0}")")"

################################################################################
#
#   Get our script directory
#
################################################################################

readonly SCRIPT_DIR="${REPO_ROOT_DIR}/scripts"

################################################################################
#
#   Android
#
################################################################################

"${SCRIPT_DIR}/android-install.sh"

################################################################################
#
#   OpenCV
#
################################################################################

"${SCRIPT_DIR}/ffmpeg-install.sh"
"${SCIPT_DIR}/opencv-install.sh"

################################################################################
#
#   matplotlib
#
################################################################################

"${SCIPT_DIR}/matplotlib-install.sh"

################################################################################
#
#   install the theano stuff
#
################################################################################

"${SCRIPT_DIR}/${THEANO_INSTALL_SCRIPT}"

